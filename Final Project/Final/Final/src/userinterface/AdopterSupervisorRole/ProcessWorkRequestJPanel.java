    private void importPersonalInfoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importPersonalInfoButtonActionPerformed
        // TODO add your handling code here:
        String filePath = "data//adopterPersonalInfo.txt";
        File file = new File (filePath);
        try {
            FileReader fr= new FileReader(file);
            BufferedReader br = new BufferedReader(new FileReader(file));

            DefaultTableModel model = (DefaultTableModel)infoTable.getModel();
            // Object[] lines = br.lines.toArray();
            String line = "";
            while(line != null) {
                line = br.readLine().trim();
                if(line != null) {
                    String[] dataRow = line.split("\t");
                    model.addRow(dataRow);
                }
            }
        } catch (Exception ex) {
            System.out.println("Exception occured");
        }
    }//GEN-LAST:event_importPersonalInfoButtonActionPerformed

