    private void importButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importButton1ActionPerformed
        // TODO add your handling code here:
        String filePath = "data//children.csv";
        try {
            BufferedReader br = new BufferedReader(new FileReader(filePath));

            DefaultTableModel model = (DefaultTableModel)childTable.getModel();
  
            String line;
            br.readLine();//skip the first line 
            while (true) {
                line = br.readLine();
                if (line == null) break;
                String[] dataRow = line.split(",");
                model.addRow(dataRow);
            }
        } catch (Exception ex) {
            System.out.println("Exception occured");
        }
    }//GEN-LAST:event_importButton1ActionPerformed

