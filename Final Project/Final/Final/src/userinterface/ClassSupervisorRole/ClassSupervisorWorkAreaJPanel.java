    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String filePath = "data//studentData.csv";
        File file = new File(filePath);
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);

            //            for (int j = 0; j < childTable.getColumnCount(); j++) {
                //                bw.write(childTable.getColumnName(j) + "\t");
                //            }
            //            bw.newLine();
            bw.write("FirstName,LastName,DOB,Age");
            bw.newLine();
            for (int i = 0; i < tblStudentdetails.getRowCount(); i++) {
                for (int j = 0; j < tblStudentdetails.getColumnCount(); j++) {
                    bw.write(tblStudentdetails.getValueAt(i, j).toString() + ",");
                }
                bw.newLine();
            }

            bw.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(ClassSupervisorWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Saved data Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

