    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

//        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
//        userProcessContainer.add("RequestLabTestJPanel", new RequestLabTestJPanel(userProcessContainer, userAccount, enterprise));
//        layout.next(userProcessContainer);
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }
        
        String request = workRequestJTable.getValueAt(selectedRow, 0).toString();

        Path path = Paths.get(request);
        String OrphanageFile = path.toString();
        File file = new File(OrphanageFile);
        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            DefaultTableModel model = (DefaultTableModel) tblStudentdetails.getModel();
            String line = "";
            while (line != null) {
                line = br.readLine().trim();
                if (line != null) {
                    String[] datarow = line.split("\t");
                    model.addRow(datarow);
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ClassSupervisorWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ClassSupervisorWorkAreaJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_requestTestJButtonActionPerformed

