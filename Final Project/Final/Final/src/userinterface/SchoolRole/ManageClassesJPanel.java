public void refreshTable(){
DefaultTableModel dtm=(DefaultTableModel)tblClass.getModel();
 dtm.setRowCount(0);
        
        for (EachClass ec : acl.getAllClassList()){
            Object[] row = new Object[4];
            row[0] = ec;
            row[1] = ec.getMinAge();
            row[2]=ec.getMaxAge();
            row[3]=ec.getClassAveragePerformance();
            dtm.addRow(row);
        }

}

