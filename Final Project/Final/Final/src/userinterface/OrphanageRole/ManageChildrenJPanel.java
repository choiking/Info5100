    private void downloadjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downloadjButtonActionPerformed
        // TODO add your handling code here:
        //File file = new File("C:\\NEU\\AED_SPRING_2018\\Project\\children.xls");

        String filePath = "data//children.csv";
        File file = new File(filePath);
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write("FirstName,LastName,DOB,Age");
            bw.newLine();
            for (int i = 0; i < childTable.getRowCount(); i++) {
                for (int j = 0; j < childTable.getColumnCount(); j++) {
                    bw.write(childTable.getValueAt(i, j).toString() + ",");
                }
                bw.newLine();
            }

            bw.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(ManageChildrenJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Report Downloaded Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_downloadjButtonActionPerformed

