    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dFormat = new SimpleDateFormat("MM-dd-yyyy");
        ChildDetails cd = new ChildDetails();
        ChildDetails cde = cdd.createChildDetails(cd);
        cde.setFirstName(firstNameTextField.getText());
        cde.setLastName(lastNameTextField.getText());
        
        
        Date currentDate = new Date();
        Date choseDate = jDateChooser1.getDate();
        if (currentDate.after(choseDate)) {    
            JOptionPane.showMessageDialog(null, "Please input previous date");
        }
      
        String date = dFormat.format(jDateChooser1.getDate());    
        cde.setDob(date);
        
        String age = ageTextField.getText();
        
        if (!age.matches("-?\\d+")) {
            JOptionPane.showMessageDialog(null, "Please input only digits");
        }
        
        
        cde.setAge(Integer.parseInt(age));  
        //refresh
        refreshTable();
        
    }//GEN-LAST:event_addButtonActionPerformed

