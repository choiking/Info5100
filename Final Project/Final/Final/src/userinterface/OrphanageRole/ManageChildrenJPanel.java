    public void refreshTable() {
        DefaultTableModel dtm = (DefaultTableModel) childTable.getModel();
        dtm.setRowCount(0);

        for (ChildDetails cd : cdd.getChildDetailsList()) {
            Object[] row = new Object[4];
            row[0] = cd;
            row[1] = cd.getLastName();
            row[2] = cd.getDob();
            row[3] = cd.getAge();
            dtm.addRow(row);
        }
    }


