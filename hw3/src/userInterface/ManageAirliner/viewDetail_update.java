    private void BtnCancelFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCancelFlightActionPerformed
        // TODO add your handling code here:
                int selectedRow =tblFlight.getSelectedRow();
        
        if(selectedRow >=0)
        {
            Flight f =(Flight) tblFlight.getValueAt(selectedRow, 0);
            airliner.getFlightSchedule().remove(f);
            JOptionPane.showMessageDialog(null, "Deleted Successfully!");
            populateTable();
        }
        else
            JOptionPane.showMessageDialog(null, "Please Select a Row!");
    }//GEN-LAST:event_BtnCancelFlightActionPerformed

    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        // TODO add your handling code here:
        int selected = tblFlight.getSelectedRow();
        if(selected >=0)
        {
            Flight f = (Flight) tblFlight.getValueAt(selected, 0);
            update panel = new update(userProcess,f,true,airliner);
            userProcess.add("UpdateFlight",panel);
            CardLayout layout = (CardLayout) userProcess.getLayout();
            layout.next(userProcess);
        }
        else
            JOptionPane.showMessageDialog(null, "Please select a row");
        
    }//GEN-LAST:event_BtnUpdateActionPerformed


